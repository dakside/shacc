    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        TransactionAction action = new TransactionAction();
        if (Account.TYPE_DEBIT_STRING.equals(cboAction.getSelectedItem())) {
            action.setAction(Account.TYPE_DEBIT);
        } else if (Account.TYPE_CREDIT_STRING.equals(cboAction.getSelectedItem())) {
            action.setAction(Account.TYPE_CREDIT);
        }
        action.setToAccount((Account) cboAccount.getSelectedItem());

        //add to model
        TransactionActionTableModel model = (TransactionActionTableModel) transactionActionTable1.getModel();
        model.add(action);
        model.fireTableDataChanged();
    }//GEN-LAST:event_btnCreateActionPerformed

