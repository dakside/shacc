    private TransactionType trxType = null;

    public TransactionType getTransactionType() {
        if (trxType == null) {
            trxType = new TransactionType();
        }

        if(txtCode.getText() == null || txtCode.getText().trim().length() == 0) return null;
        if(txtName.getText() == null || txtName.getText().trim().length() == 0) return null;

        trxType.setCode(txtCode.getText());
        trxType.setName(txtName.getText());

        trxType.getActions().clear();
        TransactionAction[] actions = transactionActionTable1.getAllActions();

        //TO DO throw exceptions
        if (actions.length < 2) return null;
        for (TransactionAction action : actions) {
            trxType.add(action);
        }
        return trxType;
    }

    public void setTransactionType(TransactionType trxType) {
        this.trxType = trxType;
        //reset UI
        txtCode.setText("");
        txtName.setText("");
        TransactionActionTableModel model = (TransactionActionTableModel) transactionActionTable1.getModel();
        model.clear();
        model.fireTableDataChanged();

        //XXX review this code later
        if (trxType != null) {
            txtCode.setText(trxType.getCode());
            txtName.setText(trxType.getName());
            transactionActionTable1.load(trxType.getActionArray());
        }
    }

    public void refresh() {
        Account[] accounts = TransactionTypeController.getInstance().getAllAccounts();
        cboAccount.removeAllItems();
        for (Account account : accounts) {
            if (account.getType() != Account.TYPE_FOLDER) {
                cboAccount.addItem(account);
            }
        }
    }
