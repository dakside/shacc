    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try{
            ExchangeRateManager manager = ExchangeRateManager.getInstance();

            CurrencyManager curManager = CurrencyManager.getInstance();
            Date fromDate = DateTimeHelper.toDate(txtFromDateNew.getText().trim(), "dd/mm/yyyy HH:mm:ss");
            Date toDate = DateTimeHelper.toDate(txtToDateNew.getText().trim(), "dd/mm/yyyy HH:mm:ss");

            Currency fromCur = null;
            Object objFromCur = comboFromCurNew.getSelectedItem();
            if(objFromCur instanceof Currency){
                fromCur = (Currency)objFromCur;
            }
            Currency toCur = null;
            Object objToCur = comboToCurNew.getSelectedItem();
            if(objToCur instanceof Currency){
                toCur = (Currency)objToCur;
            }

            if(fromCur.getIsoCode().compareTo(toCur.getIsoCode()) == 0){
                JOptionPane.showMessageDialog(this,"Currencies cannot be the same","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }

            ExchangeRateType type = null;
            Object objType = comboTypeNew.getSelectedItem();
            if(objType instanceof ExchangeRateType){
                type = (ExchangeRateType)objType;
            }

            double rates = Double.parseDouble(txtRateNew.getText().trim());
            ExchangeRate rate = null;
            if (fromCur != null && toCur != null && fromDate != null && toDate != null && type != null && rates > 0){
                rate = new ExchangeRate(fromCur, toCur, fromDate, toDate, rates, type);
                manager.save(rate);                
            }
            doClose(RET_OK);
        }
        catch(NumberFormatException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"Rate is not a valid number.","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"Unable to save result.","Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_okButtonActionPerformed

