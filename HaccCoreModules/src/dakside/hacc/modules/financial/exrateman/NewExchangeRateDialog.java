    private void loadNew(){
        try{
            ExchangeRateManager rateManager = ExchangeRateManager.getInstance();
            CurrencyManager curManager = CurrencyManager.getInstance();
            Currency[] allCur = curManager.getAllCurrency();

            //load from currency combo
            DefaultComboBoxModel modelComboFromCur = (DefaultComboBoxModel)(comboFromCurNew.getModel());
            for (Currency cur:allCur){
                modelComboFromCur.addElement(cur);
            }

            //load to currency combo
            DefaultComboBoxModel modelComboToCur = (DefaultComboBoxModel)(comboToCurNew.getModel());
            for (Currency cur:allCur){
                modelComboToCur.addElement(cur);
            }

            //load dates
            txtFromDateNew.setText(DateTimeHelper.toString(DateTimeHelper.toDateStart(new Date()),"dd/mm/yyyy HH:mm:ss"));
            txtToDateNew.setText(DateTimeHelper.toString(DateTimeHelper.toDateEnd(new Date()),"dd/mm/yyyy HH:mm:ss"));

            //load types
            DefaultComboBoxModel modelComboType = (DefaultComboBoxModel)(comboTypeNew.getModel());
            ExchangeRateType[] allTypes = rateManager.getAllExchangeRateTypes();
            for (ExchangeRateType typ:allTypes){
                modelComboType.addElement(typ);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }

